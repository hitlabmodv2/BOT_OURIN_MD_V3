export function getRandomAnimeAudio() {
  return ANIME_AUDIO_LIST[Math.floor(Math.random() * ANIME_AUDIO_LIST.length)];
}

export function getRandomAnimeAudioExcluding(excludeName) {
  const filtered = ANIME_AUDIO_LIST.filter((a) => a.name !== excludeName);
  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function getAnimeAudioByName(name) {
  return ANIME_AUDIO_LIST.find((a) => a.name === name) || null;
}

export function getAllAnimeAudio() {
  return ANIME_AUDIO_LIST;
}

export function getAudioCount() {
  return ANIME_AUDIO_LIST.length;
}

